package by.bsuir.oop.lab.t9;

public class Ball {

    private final String color;
    private final float weight;

    public Ball (String color, float weight){
        this.color = color;
        this.weight = weight;
    }

    public String getColor() {
        return color;
    }

    public float getWeight() {
        return weight;
    }
}
